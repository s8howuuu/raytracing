package raytracer.core;

public class TmpTria {
     int x;
     int y;
     int z;
     public TmpTria(int x,int y,int z) {
    	this.x = x;
    	this.y = y;
    	this.z = z;
     }
     public int getX() {
    	 return this.x;
     }
     public int getY() {
    	 return this.y;
     }
     public int getZ() {
    	 return this.z;
     }
}
